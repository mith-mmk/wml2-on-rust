pub(crate) fn tr(locale: &str, key: UiTextKey) -> &'static str {
    for candidate in resource_locale_fallbacks(locale) {
        match candidate.as_str() {
            "ja_JP" | "ja" => return ja(key),
            "en_US" | "en_GB" | "en" => return en(key),
            _ => {}
        }
    }
    en(key)
}

